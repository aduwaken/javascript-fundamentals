console.log("---Counting 1 to 10---");
for (let i=1; i<=10; i++) {
    console.log(i);
}

console.log("---Even numbers---");
for (let i=2; i<=10; i+=2) {
    console.log(i);
}

console.log("---While loop---");
let count = 10;
while (count>=1) {
    console.log(count);
    count--;
}
